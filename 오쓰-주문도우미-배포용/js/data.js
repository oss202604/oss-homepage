(function migrateOrders(){
  var changed=false;
  orders.forEach(function(o){
    if(STATUS_MIGRATE[o.status]){o.status=STATUS_MIGRATE[o.status];changed=true;}
    if(!STATUS_LABEL[o.status]){o.status='req';changed=true;}
    if(o.itemName==null&&o.itemSummary!=null){o.itemName=o.itemSummary;changed=true;}
    if(o.recipient==null){o.recipient=o.customer||'';changed=true;}
    if(o.buyYen==null){o.buyYen=o.yen||0;changed=true;}
    if(o.sellKrw==null){o.sellKrw=(o.goodsKrw||0)+(o.feeKrw||0);changed=true;}
    if(o.qty==null){o.qty=1;changed=true;}
    if(o.settled==null){o.settled='미정산';changed=true;}
    if(o.bigo==null){o.bigo='구매대행';changed=true;}
  });
  if(changed)saveOrders();
})();

